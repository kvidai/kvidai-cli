/**
 * Renders the kvidai CLI banner with gradient colors.
 * Generated from assets/banner.json — do not edit by hand.
 */
export function renderBanner(
  version: string,
  variant: "small" | "large" = "large",
): string {
  const noColor =
    !process.stdout.isTTY || "NO_COLOR" in process.env || "CI" in process.env;
  const banner = variant === "small" ? bannerSmall : bannerLarge;
  const content = banner.content(version);
  if (noColor) {
    return content.join("\n");
  }
  const chalk = new Chalk();
  return content
    .map((line, y) => {
      // Find the version string in this line so extra characters
      // beyond the original color map still get the right color.
      const versionIdx = line.indexOf(version);
      let versionColor: string | undefined;
      if (versionIdx >= 0) {
        for (let i = versionIdx; i < versionIdx + version.length; i++) {
          versionColor = banner.colors.foreground[`${i},${y}`];
          if (versionColor) break;
        }
      }

      return line
        .split("")
        .map((char, x) => {
          const color = banner.colors.foreground[`${x},${y}`];
          if (color) {
            return chalk.hex(color)(char);
          }
          if (
            versionColor &&
            versionIdx >= 0 &&
            x >= versionIdx &&
            x < versionIdx + version.length
          ) {
            return chalk.hex(versionColor)(char);
          }
          return char;
        })
        .join("");
    })
    .join("\n");
}
